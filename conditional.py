# if Statement (Basic Condition)
age = 20

if age >= 18:
    print("You are eligible to vote")

# if - else Statement
age = 16

if age >= 18:
    print("You can vote")
else:
    print("You cannot vote")

# if - elif - else (Multiple Conditions)
marks = 75

if marks >= 90:
    print("Grade A")
elif marks >= 70:
    print("Grade B")
elif marks >= 50:
    print("Grade C")
else:
    print("Fail")
