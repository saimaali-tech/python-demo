# Arithmetic Operators
a = 10
b = 3

print(a + b)    # 13
print(a - b)    # 7
print(a * b)    # 30
print(a / b)    # 3.3333
print(a % b)    # 1
print(a ** b)   # 1000
print(a // b)   # 3


# Assignment Operators
x = 10
x += 5
print(x)   # 15

x *= 2
print(x)   # 30


# Comparison (Relational) Operators

a = 10
b = 20

print(a == b)   # False
print(a != b)   # True
print(a > b)    # False
print(a < b)    # True

